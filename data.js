function generateComprehensiveData() {
    // Add more units to existing books and create new books
    for (let book = 1; book <= 6; book++) {
        if (!vocabularyData.books[book]) {
            vocabularyData.books[book] = {
                title: `Book ${book} - ${getBookLevel(book)}`,
                description: getBookDescription(book),
                units: {}
            };
        }
        
        // Generate 30 units for each book
        for (let unit = 1; unit <= 30; unit++) {
            if (!vocabularyData.books[book].units[unit]) {
                const level = getBookLevel(book).toLowerCase().split(' ')[0];
                const theme = getUnitTheme(unit, level);
                vocabularyData.books[book].units[unit] = {
                    title: `Unit ${unit}: ${theme}`,
                    description: getUnitDescription(theme),
                    words: generateThematicWords(unit, book, theme)
                };
            }
        }
    }
}

